    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
       String name =  table.getValueAt(table.getSelectedRow(), 1).toString();
       int pId =  Integer.parseInt(table.getValueAt(table.getSelectedRow(), 0).toString());
       p= orderDao.getProductByNameAndId(name, pId);
       
       unitsField.setText(Integer.toString(p.getNumOfUnits()));
       
       priceField.setText(Integer.toString(p.getPricePerUnit()));
       
    }//GEN-LAST:event_tableMouseClicked

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
       Products product= new Products();
       
       product.setName(p.getName());
       product.setStoreId(p.getStoreId());
       product.setNumOfUnits(Integer.parseInt(unitsField.getText()));
       product.setPricePerUnit(Integer.parseInt(priceField.getText()));
       
       orderDao.updateProduct(product);
       
        ProductUpdate update= new ProductUpdate();
        update.setManagerId(userId);
        update.setStoreId(p.getStoreId());
        update.setProductName(p.getName());
        Date date1= new Date();
       
        java.sql.Date date= new java.sql.Date(date1.getTime());
        update.setDate(date);
        
        orderDao.addProductUpdate(update);
       fillProductsTable();
       fillProductUpdatesTable();
    }//GEN-LAST:event_updateBtnActionPerformed

