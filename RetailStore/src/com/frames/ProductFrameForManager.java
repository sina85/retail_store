    public void fillProductUpdatesTable()
    {
        Object columnNames[]={"Update Number","Manager Id","Store Id","Product Name","Update On"};
        DefaultTableModel tableModel=new DefaultTableModel(columnNames,0);
        List<ProductUpdate> list;
        list = orderDao.getLast5Updates(userId);
        
        for(ProductUpdate update: list)
        {
         Object rowData[]={update.getUpdateNumber(),update.getManagerId(),update.getStoreId(),update.getProductName(), update.getDate()};   
         tableModel.addRow(rowData);
         table1.setModel(tableModel);
            
        }
    }
    
    
    
    
    public void fillProductsTable()
    {
        Object columnNames[]={"Store Id","Name","Number of units","price per unit"};
        DefaultTableModel tableModel=new DefaultTableModel(columnNames,0);
        List<Products> list;
        list = dao.getProductsForManager(userId);
        
        for(Products product: list)
        {
         Object rowData[]={product.getStoreId(),product.getName(), product.getNumOfUnits(),product.getPricePerUnit()};   
         tableModel.addRow(rowData);
         table.setModel(tableModel);
            
        }
    }
    
