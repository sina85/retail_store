      public void fillPopularProductsTable()
    {
        Object columnNames[]={"ProductName"};
        DefaultTableModel tableModel=new DefaultTableModel(columnNames,0);
      
        List<String> list = dao.get5PopularProducts();
 
        if(!list.isEmpty()){
        for(String name: list)
        {
         Object rowData[]={name};   
         tableModel.addRow(rowData);
         table.setModel(tableModel);
        }
        }
    }
          
      
      
      public void fillPopularCustomersTable()
    {
        Object columnNames[]={"Customer Id"};
        DefaultTableModel tableModel=new DefaultTableModel(columnNames,0);
        List<Integer> list= dao.get5PopularCustomers();
        if(!list.isEmpty()){
        for(Integer id: list)
        {
         Object rowData[]={id};   
         tableModel.addRow(rowData);
         table1.setModel(tableModel);
        }
        }
    }
    
    
    
    
    
    
