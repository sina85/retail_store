    private void requestBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestBtnActionPerformed
       int warehouseId = Integer.parseInt(warehouseIdField.getText());
       int storeId = Integer.parseInt(storeIdField.getText());
       String name= productNameField.getText();
       Products p= dao.getProductByName(name);
       int units = Integer.parseInt(unitsField.getText());
       
       SupplyRequest request= new SupplyRequest();
       
       request.setManagerId(managerId);
       request.setWarehouseId(warehouseId);
       request.setStoreId(storeId);
       request.setProductName(name);
       request.setUnits(units);

       dao.addSupplyRequest(request);
       
       
       
       
       Products p1= new Products();
       p1.setName(p.getName());
       p1.setNumOfUnits(p.getNumOfUnits()+units);
       p1.setPricePerUnit(p.getPricePerUnit());
       p1.setStoreId(p.getStoreId());
       
       dao.updateProduct(p1);
    }//GEN-LAST:event_requestBtnActionPerformed

